package com.codeclinic.yakrmdeliveryman.LocationUpdates;

public class LocationUpdatesModel {
    String id, userName, latitude, longitude;

    public LocationUpdatesModel(String id, String userName, String latitude, String longitude) {
        this.id = id;
        this.userName = userName;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }
}
